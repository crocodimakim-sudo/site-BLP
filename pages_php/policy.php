## 2. Определения

**Персональные данные** — любая информация, относящаяся к определённому или определяемому физическому лицу (субъекту персональных данных).

**Обработка персональных данных** — сбор, запись, систематизация, накопление, хранение, уточнение (обновление, изменение), извлечение, использование, передача, блокирование, удаление и уничтожение персональных данных.

**Оператор** — юридическое лицо, определяющее цели и средства обработки персональных данных.

