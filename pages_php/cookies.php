## 1. Общие положения

Веб-сайт «building-port.ru/blp» (далее — «**Сайт**») использует технологию файлов cookies и аналогичные механизмы отслеживания для улучшения опыта пользователя и сбора статистической информации.

Настоящая Политика в отношении файлов cookies (далее — «**Политика cookies**») описывает, какие файлы cookies мы используем, для каких целей, и как Вы можете их контролировать.

