## 5. СОГЛАСИЕ НА ИСПОЛЬЗОВАНИЕ COOKIES

### 5.1. Требования закона

В соответствии с требованиями электронного коммуникационного законодательства и Федерального закона от 27 июля 2006 г. № 152-ФЗ:

- Компания должна получить Ваше согласие перед установкой **необязательных** cookies
- Технические cookies могут быть установлены без согласия
- Согласие должно быть дано **отдельно** от других документов

### 5.2. Как мы получаем согласие

Компания использует **banner с согласием на cookies** (cookie consent banner), который появляется при первом посещении Сайта:

- **Принять всё** — согласие на все типы cookies
- **Отклонить** — отказ от необязательных cookies
- **Управление настройками** — выбор конкретных типов cookies

### 5.3. Сохранение выбора

Ваш выбор сохраняется в cookie-файле сроком на 1 год, и мы не будем показывать banner повторно, пока файл не будет удалён.

### 5.4. Отозыв согласия

Вы можете в любой момент:
- Изменить свои предпочтения через banner (если он доступен)
- Связаться с нами: info@building-port.ru
- Удалить cookies через браузер

