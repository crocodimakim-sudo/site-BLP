## 6. УПРАВЛЕНИЕ COOKIES И КОНТРОЛЬ

### 6.1. Управление cookies через браузер

Большинство браузеров позволяют управлять cookies через параметры:

#### Google Chrome:
1. Откройте Chrome → **Параметры** (⋮)
2. **Конфиденциальность и безопасность** → **Cookies и другие данные сайтов**
3. Выберите опцию управления cookies

#### Mozilla Firefox:
1. Откройте Firefox → **Параметры**
2. **Конфиденциальность и безопасность**
3. Раздел **Cookies и данные веб-сайтов**

#### Safari (Mac/iOS):
1. **Safari** → **Параметры**
2. **Конфиденциальность**
3. Отметьте опции для управления cookies

#### Microsoft Edge:
1. Откройте Edge → **Параметры** (⋯)
2. **Конфиденциальность, поиск и услуги**
3. **Cookies и другие данные сайтов**

