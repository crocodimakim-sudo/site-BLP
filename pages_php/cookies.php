## 2. Что такое файлы cookies?

**Файлы cookies** — это небольшие текстовые файлы, которые сохраняются на жёстком диске Вашего компьютера, мобильного телефона, планшета или иного устройства (далее — «**Устройство**») при посещении веб-сайтов.

### 2.1. Как работают cookies?

- Когда Вы посещаете Сайт, сервер отправляет файл cookies на Ваше Устройство
- Браузер сохраняет этот файл локально
- При следующих посещениях Сайта браузер отправляет этот файл обратно серверу
- Это позволяет серверу узнать, что это Вы, и восстановить Вашу историю и предпочтения

### 2.2. Размер и содержимое

- Типичный файл cookies занимает 4-5 килобайт
- Содержит идентификатор сессии, дату создания и срок действия
- Не содержит исполняемый код или вирусы (это просто текстовый файл)

